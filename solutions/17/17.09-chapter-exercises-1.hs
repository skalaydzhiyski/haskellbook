-- Given a type that has an instance of `Applicative`, specialize the types
-- of the methods.
--
-----------------------------------------------------------------------------
-- 1.
-- []
pure  :: a -> [a]
(<*>) :: [(a -> b)] -> [a] -> [b]

-----------------------------------------------------------------------------
-- 2.
-- IO
pure  :: a -> IO a
(<*>) :: IO (a -> b) -> IO a -> IO b

-----------------------------------------------------------------------------
-- 3.
-- (,) a
pure  :: Monoid a =>  b -> (a, b)
(<*>) :: Monoid c => (c, (a -> b)) -> (c, a) -> (c, b)

-----------------------------------------------------------------------------
-- 4.
-- (->) e
pure  :: a -> (e -> a)
(<*>) :: e -> (a -> b) -> (e -> a) -> (e -> b)

