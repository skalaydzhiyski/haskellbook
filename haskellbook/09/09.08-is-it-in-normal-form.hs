-- For each expression, determin whether its in:
-- 1. normal form, which implies weak head normal form
-- 2. weak head normal form only
-- 3. neither

-----------------------------------------------------------------------------
-- 1.
-- [1, 2, 3, 4, 5]
-- Answer:
-- normal form only

-----------------------------------------------------------------------------
-- 2.
-- 1 : 2 : 3 : 4 : _
-- Answer:
-- weak head normal form

-----------------------------------------------------------------------------
-- 3.
-- enumFromTo 1 10
-- Answer:
-- neither

-----------------------------------------------------------------------------
-- 4.
-- length [1, 2, 3, 4, 5]
-- Answer:
-- neither

-----------------------------------------------------------------------------
-- 5.
-- sum (enumFromTo 1 10)
-- Answer:
-- neither

-----------------------------------------------------------------------------
-- 6.
-- ['a'..'m'] ++ ['n'..'z']
-- Answer:
-- neither

-----------------------------------------------------------------------------
-- 7.
--- (_, 'b')
-- Answer:
-- weak head normal form
